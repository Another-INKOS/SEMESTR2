// ======================================================================
// 1. ИНТЕРФЕЙСНЫЙ КЛАСС (все методы чисто виртуальные, нет полей)
//    Иерархия начинается с этого класса.
// ======================================================================

class ICalendar {
public:
    // Виртуальная функция позволяет вызывать правильную версию метода через указатель/ссылку на базовый класс
    // Чисто виртуальный метод: =0 значит, что у него нет реализации в этом классе
    // Любой класс-наследник ОБЯЗАН переопределить этот метод
    // const означает, что метод не изменяет поля объекта. Обещает, что метод не изменяет состояние объекта
    virtual void Show() const = 0;
    
    // Чисто виртуальный метод, возвращает double (число с плавающей точкой)
    // В праздничном календаре вернёт количество праздников
    // В рабочем календаре вернёт приблизительное число рабочих дней в году
    virtual double GetAnnualValue() const = 0;
    
    // Виртуальный деструктор. Обязателен в иерархиях!
    // При удалении объекта через указатель ICalendar* вызовется
    // правильный деструктор класса-наследника
    virtual ~ICalendar() = 0;   // =0 означает чисто виртуальный, но тело всё равно нужно
};

// Тело чисто виртуального деструктора. Оно нужно, потому что
// деструктор базового класса всё равно вызывается при удалении объекта
ICalendar::~ICalendar() {}

// ======================================================================
// 2. ШАБЛОННЫЙ БАЗОВЫЙ КЛАСС (T – тип года, например int или long long)
//    Реализует общие поля и методы для всех календарей.
//    template <typename T> означает, что класс может работать с любым типом
// ======================================================================

template <typename T>   // T — параметр шаблона (тип данных для года)
class CalendarTemplate : public ICalendar {   // наследуем интерфейс ICalendar

private:
    // СТАТИЧЕСКОЕ ПОЛЕ: существует в единственном экземпляре для всех объектов
    // Считает общее количество когда-либо созданных календарей
    // static означает, что поле принадлежит классу, а не объекту
    static int totalCount;

protected:
    // ИНКАПСУЛЯЦИЯ: поля защищены (protected) – доступны наследникам, но не внешнему коду
    string name;        // название календаря (например, "Григорианский")
    T year;             // год. Тип T подставится при создании объекта (int, long long и т.д.)
    int daysInYear;     // количество дней в году (только 365 или 366)

public:
    // ========== КОНСТРУКТОРЫ ==========
    
    // Конструктор по умолчанию (без параметров)
    // Вызывается, когда создают объект без аргументов
    // Двоеточие после скобок — список инициализации (поля заполняются до входа в тело)
    CalendarTemplate() : name("Неизвестно"), year(0), daysInYear(365) {
        totalCount++;   // увеличиваем общий счётчик при создании любого календаря
    }

    // Конструктор с параметрами
    // const string& n — ссылка на строку (чтобы не копировать)
    // T y — год типа T
    // bool isLeap = false — параметр по умолчанию, если не указан, то false
    CalendarTemplate(const string& n, T y, bool isLeap = false)
        : name(n), year(y), daysInYear(isLeap ? 366 : 365) {   // если isLeap true, то 366, иначе 365
        totalCount++;   // увеличиваем общий счётчик при создании любого календаря
        
        // ОГРАНИЧЕНИЕ: количество дней в году не может быть меньше 365
        if (daysInYear < 365) daysInYear = 365;
        // и не может быть больше 366
        if (daysInYear > 366) daysInYear = 366;
    }

    // ========== ДЕСТРУКТОР ==========
    // Виртуальный деструктор — чтобы правильно уничтожались объекты-наследники
    // Тело пустое, так как динамической памяти в этом классе нет
    virtual ~CalendarTemplate() {}

    // ========== ПОЛИМОРФНЫЕ МЕТОДЫ ==========
    
    // Виртуальный метод Show() — переопределяет метод из ICalendar
    // override — ключевое слово, проверяет, что метод действительно переопределяет
    // родительский. const — метод не меняет поля объекта
    virtual void Show() const override {
        cout << "Календарь: " << name << endl;      // выводим название
        cout << "\tГод: " << year << endl;          // выводим год
        cout << "\tДней в году: " << daysInYear << endl;  // выводим количество дней
    }

    // Второй полиморфный метод — чисто виртуальный
    // =0 означает, что у этого метода нет реализации в базовом классе
    // Каждый потомок ОБЯЗАН его реализовать
    virtual double GetAnnualValue() const override = 0;

    // ========== СТАТИЧЕСКИЕ МЕТОДЫ ==========
    
    // Статический метод: можно вызвать без создания объекта
    // Вызывается через имя класса: CalendarTemplate<int>::ShowTotalCount()
    static void ShowTotalCount() {
        // Выводим значение статического поля totalCount
        cout << "Всего создано календарей (всех типов): " << totalCount << endl;
    }

    // ========== ГЕТТЕРЫ И СЕТТЕРЫ (ИНКАПСУЛЯЦИЯ) ==========
    
    // Геттер для name — возвращает строку. const — метод не меняет объект
    string GetName() const { return name; }
    
    // Геттер для year — возвращает год типа T
    T GetYear() const { return year; }
    
    // Геттер для daysInYear — возвращает количество дней
    int GetDaysInYear() const { return daysInYear; }

    // Сеттер для name — изменяет название. Принимает константную ссылку
    void SetName(const string& n) { name = n; }
    
    // Сеттер для year — изменяет год
    void SetYear(T y) { year = y; }
    
    // Сеттер для дней в году — снова применяет ограничение 365–366
    void SetDaysInYear(int days) {
        if (days < 365) days = 365;   // если меньше 365, ставим 365
        if (days > 366) days = 366;   // если больше 366, ставим 366
        daysInYear = days;            // присваиваем новое значение
    }

    // ========== ПЕРЕГРУЗКА ОПЕРАТОРА << ==========
    
    // friend — дружественная функция (не член класса, но имеет доступ к полям). нужно для того, чтобы создавать более гибкие и мощные конструкции классов
    // operator<< — перегрузка оператора вывода
    // ostream& os — ссылка на поток вывода (cout)
    // const CalendarTemplate<T>& cal — константная ссылка на объект
    // Возвращает ostream&, чтобы можно было писать cout << a << b << endl;
    friend ostream& operator<<(ostream& os, const CalendarTemplate<T>& cal) {
        cal.Show();   // внутри вызываем виртуальный метод Show()
        return os;    // возвращаем поток для цепочки вызовов
    }
};

// ========== ИНИЦИАЛИЗАЦИЯ СТАТИЧЕСКОГО ПОЛЯ ==========
// Статические поля нужно ОПРЕДЕЛИТЬ вне класса (выделить память)
// template <typename T> — для каждого инстанцирования шаблона своё поле
template <typename T>
int CalendarTemplate<T>::totalCount = 0;   // начальное значение 0

// ======================================================================
// 3. ПЕРВЫЙ ПОТОМОК — ПРАЗДНИЧНЫЙ КАЛЕНДАРЬ (HolidayCalendar)
//    Наследует CalendarTemplate, добавляет поле holidayCount
//    Демонстрирует НАСЛЕДОВАНИЕ и ПОЛИМОРФИЗМ
// ======================================================================

template <typename T>
class HolidayCalendar : public CalendarTemplate<T> {   // публичное наследование

private:
    int holidayCount;               // количество праздничных дней в году
    static int holidayObjectCount;  // статический счётчик объектов этого типа

public:
    // ========== КОНСТРУКТОР ПО УМОЛЧАНИЮ ==========
    HolidayCalendar() : CalendarTemplate<T>(), holidayCount(0) {
        holidayObjectCount++;   // увеличиваем счётчик праздничных календарей
    }

    // ========== КОНСТРУКТОР С ПАРАМЕТРАМИ ==========
    // Вызывает конструктор родителя через список инициализации
    // Затем инициализирует holidayCount
    HolidayCalendar(const string& name, T year, bool isLeap, int holidays)
        : CalendarTemplate<T>(name, year, isLeap), holidayCount(holidays) {
        holidayObjectCount++;   // увеличиваем счётчик праздничных календарей

        // ========== ПАСХАЛКА (ТРЕТЬЕ СЕНТЯБРЯ) ==========
        // Преобразуем название в нижний регистр для поиска
        string lowerName = name;                         // копируем название
        for (auto& c : lowerName) c = tolower(c);        // каждый символ в нижний регистр
        
        // Проверяем, есть ли в названии слово "сентябрь"
        // string::npos означает "не найдено". Если find() вернул не npos, значит подстрока есть
        if (lowerName.find("сентябрь") != string::npos ||
            lowerName.find("3 сентября") != string::npos ||
            holidays == 3) {   // шутка: если праздников ровно 3, тоже выводим песню
            // Выводим цитату из песни Михаила Шуфутинского
            cout << "\n🎵🎵🎵 ПАСХАЛКА! 🎵🎵🎵\n";
            cout << "\"Я календарь переверну — и снова третье сентября...\"\n";
            cout << "Шуфутинский одобряет этот календарь!\n\n";
        }
    }

    // ========== ДЕСТРУКТОР ==========
    ~HolidayCalendar() {}

    // ========== ПЕРЕОПРЕДЕЛЕНИЕ ПОЛИМОРФНОГО МЕТОДА Show() ==========
    // Сначала вызываем родительский метод (вывод общих полей)
    // Затем добавляем свои строки
    virtual void Show() const override {
        CalendarTemplate<T>::Show();   // явный вызов метода родителя
        cout << "\tТип: Праздничный календарь" << endl;
        cout << "\tКоличество праздников: " << holidayCount << endl;
    }

    // ========== РЕАЛИЗАЦИЯ ВТОРОГО ПОЛИМОРФНОГО МЕТОДА ==========
    // Возвращаем количество праздников (как double)
    virtual double GetAnnualValue() const override {
        return static_cast<double>(holidayCount);   // преобразуем int в double
    }

    // ========== УНИКАЛЬНЫЙ МЕТОД (только для этого класса) ==========
    int GetHolidayCount() const { return holidayCount; }

    // ========== СТАТИЧЕСКИЙ МЕТОД ДЛЯ ВЫВОДА СЧЁТЧИКА ==========
    static void ShowHolidayCount() {
        cout << "Всего создано праздничных календарей: " << holidayObjectCount << endl;
    }

    // ========== ПЕРЕГРУЗКА ОПЕРАТОРА << ==========
    friend ostream& operator<<(ostream& os, const HolidayCalendar<T>& cal) {
        cal.Show();      // вызываем виртуальный Show()
        return os;       // возвращаем поток
    }
};

// Инициализация статического счётчика праздничных календарей
template <typename T>
int HolidayCalendar<T>::holidayObjectCount = 0;

// ======================================================================
// 4. ВТОРОЙ ПОТОМОК — РАБОЧИЙ КАЛЕНДАРЬ (WorkCalendar)
//    Наследует CalendarTemplate, добавляет поле workDaysPerWeek
//    Демонстрирует НАСЛЕДОВАНИЕ и ПОЛИМОРФИЗМ
// ======================================================================

template <typename T>
class WorkCalendar : public CalendarTemplate<T> {

private:
    int workDaysPerWeek;        // количество рабочих дней в неделе (обычно 5 или 6)
    static int workObjectCount;  // статический счётчик объектов этого типа

public:
    // ========== КОНСТРУКТОР ПО УМОЛЧАНИЮ ==========
    WorkCalendar() : CalendarTemplate<T>(), workDaysPerWeek(5) {
        workObjectCount++;   // увеличиваем счётчик рабочих календарей
    }

    // ========== КОНСТРУКТОР С ПАРАМЕТРАМИ ==========
    WorkCalendar(const string& name, T year, bool isLeap, int workDays)
        : CalendarTemplate<T>(name, year, isLeap), workDaysPerWeek(workDays) {
        workObjectCount++;   // увеличиваем счётчик рабочих календарей
    }

    // ========== ДЕСТРУКТОР ==========
    ~WorkCalendar() {}

    // ========== ПЕРЕОПРЕДЕЛЕНИЕ ПОЛИМОРФНОГО МЕТОДА Show() ==========
    virtual void Show() const override {
        CalendarTemplate<T>::Show();   // выводим общие поля родителя
        cout << "\tТип: Рабочий календарь" << endl;
        cout << "\tРабочих дней в неделе: " << workDaysPerWeek << endl;
    }

    // ========== РЕАЛИЗАЦИЯ ВТОРОГО ПОЛИМОРФНОГО МЕТОДА ==========
    // Возвращаем приблизительное количество рабочих дней в году
    // 52 недели × рабочие дни в неделе
    virtual double GetAnnualValue() const override {
        return 52.0 * workDaysPerWeek;
    }

    // ========== УНИКАЛЬНЫЙ МЕТОД (только для этого класса) ==========
    int GetWorkDaysPerWeek() const { return workDaysPerWeek; }

    // ========== СТАТИЧЕСКИЙ МЕТОД ДЛЯ ВЫВОДА СЧЁТЧИКА ==========
    static void ShowWorkCount() {
        cout << "Всего создано рабочих календарей: " << workObjectCount << endl;
    }

    // ========== ПЕРЕГРУЗКА ОПЕРАТОРА << ==========
    friend ostream& operator<<(ostream& os, const WorkCalendar<T>& cal) {
        cal.Show();
        return os;
    }
};

// Инициализация статического счётчика рабочих календарей
template <typename T>
int WorkCalendar<T>::workObjectCount = 0;

